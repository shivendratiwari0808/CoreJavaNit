/* 3. Write a Java program to print multiplication table by using for loop?
input : 5.
output :-

5 X 1 = 5.
5 X 2 = 10.
.
.
.
5 X 10 = 50.
 */



class Table1{


public static void main(String[]args){

	int t=5,i;


for(i=1;i<=10;i++)
{
System.out.println(t+"x"+i+"*"+t*i);

}
}
}


