public class TimeofDay
{
public static void main(String[] args){
int t=15;
if(t>=5 && t<12){
System.out.println("good morning");
}
else if(t>=12 && t<14){
System.out.println("good after noon");
}

else if(t>=14 && t<17){
System.out.println("good evening");
}

else if(t>=17 && t<22){
System.out.println("good night");
}
}}


